/**
 * Token Treemap Component
 *
 * webpack-bundle-analyzer style token usage visualization
 * Rectangle size = token ratio, color = category/severity
 *
 * + Real-time prompt feed (breaking news style)
 */

import { useState, useEffect, useCallback, useRef } from 'react';
import { Treemap, ResponsiveContainer, Tooltip } from 'recharts';
import './TokenTreemap.css';

// Prompt history type
type PromptHistory = {
  id: string;
  timestamp: string;
  content: string;
  fullContent?: string;
  tokens: number;
};

// Prompt analysis result type
type PromptAnalysis = {
  promptId: string;
  prompt: {
    content: string;
    tokens: number;
    timestamp: string;
  };
  response: {
    model: string;
    inputTokens: number;
    outputTokens: number;
    cacheCreationTokens: number;
    cacheReadTokens: number;
    totalTokens: number;
  } | null;
  cost: {
    input: number;
    output: number;
    cache: number;
    total: number;
    saved: number;
  };
};

// Treemap data type
type TreemapNode = {
  name: string;
  originalName?: string; // Original category name (CATEGORY_INFO key)
  size?: number;
  tokens?: number;
  percentage?: number;
  color?: string;
  children?: TreemapNode[];
  // Cache details
  cacheDetails?: CacheUsageItem[];
  claudeMdPreview?: string;
  // Status info
  status?: 'warning' | 'good' | 'info' | 'neutral';
  statusBadge?: string;
};

// Cache usage item type
type CacheUsageItem = {
  timestamp: string;
  prompt: string;
  cacheRead: number;
  cacheCreation: number;
  inputTokens: number;
};

// API response type
type ScanData = {
  breakdown: {
    claudeMd: { global: number; project: number; total: number };
    userInput: number;
    cacheCreation: number;
    cacheRead: number;
    output: number;
    total: number;
  };
  claudeMdSections?: Array<{
    section: string;
    tokens: number;
    percentage: number;
  }>;
  cacheInfo?: {
    claudeMdPreview: string;
    recentCacheUsage: CacheUsageItem[];
    cacheHitRate: number;
  };
};

type TokenTreemapProps = {
  onBack: () => void;
};

